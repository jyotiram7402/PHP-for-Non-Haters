<?php

/*
===========================
PHP Arithmetic Operators
===========================

Arithmetic operators are used to perform common mathematical operations between numerical values.
*/

/*
===========================
List of Arithmetic Operators
===========================

1. Addition (+)
   Adds two values.
   Example: $a + $b

2. Subtraction (-)
   Subtracts one value from another.
   Example: $a - $b

3. Multiplication (*)
   Multiplies two values.
   Example: $a * $b

4. Division (/)
   Divides one value by another.
   Example: $a / $b

5. Modulus (%)
   Returns the remainder of a division.
   Example: $a % $b

6. Exponentiation (**)
   Raises a number to the power of another.
   Example: $a ** $b
*/

/*
===========================
Example: Using Arithmetic Operators
===========================
*/

$a = 10;
$b = 3;

echo "a = $a, b = $b\n";
echo "Addition: a + b = " . ($a + $b) . "\n";
echo "Subtraction: a - b = " . ($a - $b) . "\n";
echo "Multiplication: a * b = " . ($a * $b) . "\n";
echo "Division: a / b = " . ($a / $b) . "\n";
echo "Modulus: a % b = " . ($a % $b) . "\n";
echo "Exponentiation: a ** b = " . ($a ** $b) . "\n";

?>
